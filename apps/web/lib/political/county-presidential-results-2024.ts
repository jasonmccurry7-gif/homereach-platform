export type CountyHistoricalLean =
  | "democrat"
  | "republican"
  | "mixed"
  | "unknown";

export type HistoricalMapTone = "primary" | "secondary" | "watch";

const OHIO_2024_PRESIDENTIAL_BY_COUNTY_NAME = Object.values(
  OHIO_2024_PRESIDENTIAL_BY_COUNTY_FIPS,
).reduce<Record<string, CountyPresidentialResult2024>>((lookup, result) => {
  lookup[normalizeCountyLookupName(result.county)] = result;
  return lookup;
}, {});

function normalizeCountyLookupName(value: string) {
  return value
    .toLowerCase()
    .replace(/\s+county$/, "")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

export function getOhioCountyPresidentialResult2024ByName(countyName: string) {
  return (
    OHIO_2024_PRESIDENTIAL_BY_COUNTY_NAME[
      normalizeCountyLookupName(countyName)
    ] ?? null
  );
}

export function getCountyHistoricalLean(
  result: CountyPresidentialResult2024 | null | undefined,
): CountyHistoricalLean {
  if (!result) return "unknown";

  const marginPct = result.repPct - result.demPct;
  if (Math.abs(marginPct) < 5) return "mixed";

  return marginPct > 0 ? "republican" : "democrat";
}

export function ohioHistoricalMapTone(countyName: string): HistoricalMapTone {
  const lean = getCountyHistoricalLean(
    getOhioCountyPresidentialResult2024ByName(countyName),
  );

  if (lean === "democrat") return "primary";
  if (lean === "republican") return "secondary";

  return "watch";
}

export function ohioHistoricalMapLabel(countyName: string) {
  const result = getOhioCountyPresidentialResult2024ByName(countyName);
  if (!result) return `${countyName}: historical result not loaded`;

  const marginPct = Number((result.repPct - result.demPct).toFixed(2));
  const lean =
    Math.abs(marginPct) < 5
      ? "within 5 points"
      : marginPct > 0
        ? `Republican +${Math.abs(marginPct).toFixed(2)}`
        : `Democratic +${Math.abs(marginPct).toFixed(2)}`;

  return `${result.county} County: ${lean} in 2024 presidential results`;
}

