// ── Spots ─────────────────────────────────────────────────────────────────────

export type SpotStatus = "available" | "reserved" | "in-progress" | "sold";

export interface Spot {
  id: string;
  cityId: string;
  cityName: string;
  categoryId: string;
  categoryName: string;
  status: SpotStatus;
  businessId: string | null;
  businessName: string | null;
  reservedUntil: string | null;  // ISO timestamp
  basePrice: number;
  isFullCard: boolean;
}

export interface CityAvailability {
  cityId: string;
  cityName: string;
  totalSpots: number;
  availableSpots: number;
  reservedSpots: number;
  soldSpots: number;
  isFullCardAvailable: boolean;
  fillPercent: number;
  urgencyLevel: "low" | "medium" | "high" | "critical";
  spots: Spot[];
}

export interface CategoryAvailability {
  categoryId: string;
  categoryName: string;
  cityId: string;
  status: SpotStatus;
  reservedUntil: string | null;
  isLocked: boolean;
}

// ── Pricing ───────────────────────────────────────────────────────────────────

export type DiscountType = "founding" | "military" | "promo" | "bundle" | "multispot";

export interface DiscountConfig {
  type: DiscountType;
  label: string;
  amount: number;       // absolute $ or percent
  isPercent: boolean;
  stackable: boolean;   // can combine with other discounts
  expiresAt?: string;   // ISO timestamp; undefined = never expires
}

export interface PricingContext {
  basePrice: number;
  spotCount: number;           // number of spots selected
  isFullCard: boolean;
  isFoundingMember: boolean;
  isMilitary: boolean;
  promoCode?: string;
  bundleIds?: string[];
}

export interface AppliedDiscount {
  type: DiscountType;
  label: string;
  savings: number;            // $ saved
}

export interface PriceResult {
  originalPrice: number;      // before any discounts
  finalPrice: number;         // after all discounts
  totalSavings: number;
  appliedDiscounts: AppliedDiscount[];
  perSpotPrice: number;       // finalPrice / spotCount
  billingLabel: string;       // e.g. "per month, locked in"
  isFoundingRate: boolean;
  displayOriginal: boolean;   // show strikethrough?
}

