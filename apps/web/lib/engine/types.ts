// ── Targeted Route Campaigns ──────────────────────────────────────────────────

/** USPS carrier route with household count + demographic signals */
export interface CarrierRoute {
  id: string;
  cityId: string;
  cityName: string;
  routeCode: string;             // e.g. "OH-MED-C001"
  name: string;                  // human-readable neighborhood/area name
  households: number;
  available: boolean;

  /** Demographic indicators — mock for now, real filters added later */
  demographics: {
    medianHomeValue: string;     // "$200k–$300k"
    medianIncome: string;        // "$65k–$85k"
    homeownerPct: number;        // 78
    primaryAgeGroup: string;     // "35–54"
  };

  /** Future-ready targeting fields — structure only, no logic yet */
  targetingFilters: {
    incomeRange?: string;        // "50k-75k" | "75k-100k" | "100k+"
    homeValueRange?: string;     // "150k-250k" | "250k-400k" | "400k+"
    zipCluster?: string;         // zip code grouping
  };
}

/** Pricing tier based on total household reach */
export interface RoutePricingTier {
  id: string;
  label: string;                 // "Starter" | "Growth" | "Scale" | "Enterprise"
  minHouseholds: number;
  maxHouseholds: number;         // Infinity for top tier
  pricePerThousand: number;      // $ per 1,000 households
  description: string;
}

export type TargetedCampaignStatus =
  | "draft"
  | "pending_review"
  | "active"
  | "completed"
  | "cancelled";

/** A full targeted route campaign record */
export interface TargetedCampaign {
  id: string;
  campaignType: "targeted";       // distinguish from shared postcard

  // Business / contact
  businessId: string | null;
  businessName: string;
  contactName: string;
  phone: string;
  email: string;

  // Geography
  cityId: string;
  city: string;
  selectedRouteIds: string[];     // which CarrierRoute.id entries are selected
  totalHouseholds: number;

  // Pricing
  pricePerThousand: number;
  totalPrice: number;
  pricingTierLabel: string;       // e.g. "Growth"

  // Future-ready targeting
  targetingFilters: {
    incomeRange?: string;
    homeValueRange?: string;
    zipCluster?: string;
  };

  // Status + lifecycle
  status: TargetedCampaignStatus;
  createdAt: string;
  startDate?: string;
  endDate?: string;
  notes?: string;

  // Dashboard integration flags
  appearsInAdminDashboard: boolean;
  appearsInClientDashboard: boolean;

  // Reach metrics (populated post-campaign)
  metrics?: {
    homesReached: number;
    responseRate?: number;        // %
    leadsGenerated?: number;
    estimatedROI?: number;        // $
  };
}
